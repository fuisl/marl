"""Train a GAT-based MARL agent on the 5×5 SUMO grid environment.

Wires together:
  - ``TrafficSignalEnv`` over real SUMO (via libsumo, no GUI)
  - ``GraphEncoder`` (2-layer GATv2Conv) — learns graph dynamics
  - ``SharedDiscreteActor`` + ``CentralizedTwinCritic`` (Discrete SAC)
  - Simple ring-buffer replay + manual SAC update loop

Requirements
------------
- ``libsumo`` available in the active Python env (``import libsumo`` must work)
- ``nets/grid5x5/`` generated by ``python scripts/build_network.py``
- Set ``LIBSUMO_AS_TRACI=1`` in the environment (done automatically below)

Usage
-----
    # Default: 5×5 grid, configs/env.yaml + configs/model.yaml
    python scripts/train_gat_baseline.py

    # Custom settings
    python scripts/train_gat_baseline.py --episodes 200 --batch-size 128 \\
        --net nets/grid5x5/grid5x5.net.xml \\
        --route nets/grid5x5/grid5x5.rou.xml

Outputs
-------
- Console logs (every ``--log-interval`` episodes)
- ``<out-dir>/train_log.csv``  — per-episode metrics
- ``<out-dir>/best_agent.pt``  — checkpoint of best-return agent state dict
"""

