def train(args: argparse.Namespace) -> None:
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"[train_gat_baseline] device={device}")
    print(f"  net:   {args.net}")
    print(f"  route: {args.route}")

    torch.manual_seed(args.seed)
    random.seed(args.seed)

    # --- Environment (real SUMO via libsumo) ---
    env = TrafficSignalEnv(
        net_file=args.net,
        route_file=args.route,
        delta_t=args.delta_t,
        min_green_duration=args.min_green,
        yellow_duration=args.yellow_duration,
        all_red_duration=args.all_red_duration,
        reward_mode=args.reward_mode,
        begin_time=0,
        end_time=args.end_time,
        sumo_binary="sumo",
        gui=False,
    )

    td0 = env.reset()
    obs_dim     = int(td0["agents", "observation"].shape[-1])
    num_actions = env.num_actions
    n_agents    = env.n_agents
    n_edges     = int(td0["edge_index"].shape[1])
    print(
        f"  agents={n_agents}  obs_dim={obs_dim}"
        f"  num_actions={num_actions}  graph_edges={n_edges}"
    )

    # --- Agent ---
    encoder_cfg = {
        "hidden_dim": args.hidden_dim,
        "out_dim":    args.latent_dim,
        "heads":      args.gat_heads,
        "edge_dim":   2,          # [distance_m, n_lanes]
        "dropout":    args.dropout,
    }
    actor_cfg  = {"hidden_dim": args.hidden_dim}
    critic_cfg = {"hidden_dim": args.hidden_dim * 2}

    agent = MARLDiscreteSAC(
        obs_dim=obs_dim,
        num_actions=num_actions,
        encoder_cfg=encoder_cfg,
        actor_cfg=actor_cfg,
        critic_cfg=critic_cfg,
        init_alpha=args.init_alpha,
        tau=args.tau,
    ).to(device)

    total_params = sum(p.numel() for p in agent.parameters())
    print(f"  agent params: {total_params:,}")

    # --- Optimizers ---
    opt_enc_actor = Adam(
        list(agent.encoder.parameters()) + list(agent.actor.parameters()),
        lr=args.lr,
    )
    opt_critic = Adam(agent.critic.parameters(), lr=args.lr)
    opt_alpha  = Adam([agent.log_alpha], lr=args.lr)
    optimizers = {"actor": opt_enc_actor, "critic": opt_critic, "alpha": opt_alpha}

    # --- Loss & Replay ---
    loss_fn = DiscreteSACLossComputer(agent, gamma=args.gamma)
    replay  = ReplayBuffer(args.replay_capacity, seed=args.seed)

    # --- W&B ---
    use_wandb = _WANDB_AVAILABLE and not args.no_wandb
    if use_wandb:
        # Resolve API key: CLI arg > WANDB_API_KEY env var > stored credentials
        api_key = args.wandb_api_key or os.environ.get("WANDB_API_KEY")
        if api_key:
            wandb.login(key=api_key, relogin=True)
        wandb.init(
            project=args.wandb_project,
            name=args.wandb_run_name,
            config=vars(args),
            dir=str(Path(args.out_dir)),
            tags=["gat", "discrete-sac", "marl", "sumo-5x5"],
        )
        wandb.define_metric("episode")
        wandb.define_metric("*", step_metric="episode")

    # --- Logging ---
    out_dir  = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    log_path = out_dir / "train_log.csv"
    fieldnames = [
        "episode", "n_steps", "return", "return_ma50",
        "critic_loss", "actor_loss", "alpha_loss",
        "q1", "q2", "entropy", "alpha",
        "total_transitions", "elapsed_s",
    ]
    log_f  = log_path.open("w", newline="")
    writer = csv.DictWriter(log_f, fieldnames=fieldnames)
    writer.writeheader()

    # --- Training ---
    best_return       = float("-inf")
    total_transitions = 0
    return_history: collections.deque[float] = collections.deque(maxlen=50)
    last_metrics: dict[str, Any] = {}
    t0 = time.perf_counter()

    print(f"\n{'Ep':>6}  {'Return':>10}  {'MA-50':>10}  {'CriticL':>9}"
          f"  {'ActorL':>9}  {'Entropy':>9}  {'Alpha':>7}  {'Trans':>7}")
    print("-" * 85)

    for ep in range(1, args.episodes + 1):
        agent.train()
        transitions, ep_return, ep_steps = run_episode(env, agent, device)

        for t in transitions:
            replay.push(t)
        total_transitions += len(transitions)

        return_history.append(ep_return)
        ma50 = sum(return_history) / len(return_history)

        # --- Gradient updates (one per new transition, after warmup) ---
        if total_transitions >= args.warmup:
            for _ in range(len(transitions) * args.updates_per_step):
                m = sac_update(loss_fn, replay, optimizers, args.batch_size, device)
                if m is not None:
                    last_metrics = m
            agent.soft_update_target()

        # --- Checkpoint ---
        if ep_return > best_return:
            best_return = ep_return
            torch.save(agent.state_dict(), out_dir / "best_agent.pt")

        # --- Log CSV ---
        elapsed = time.perf_counter() - t0
        row: dict[str, Any] = {
            "episode":           ep,
            "n_steps":           ep_steps,
            "return":            round(ep_return, 3),
            "return_ma50":       round(ma50, 3),
            "total_transitions": total_transitions,
            "elapsed_s":         round(elapsed, 1),
            **{k: round(float(v), 5) for k, v in last_metrics.items()},
        }
        writer.writerow(row)
        log_f.flush()

        # --- W&B ---
        if use_wandb:
            wandb.log({
                "episode":           ep,
                "return":            ep_return,
                "return_ma50":       ma50,
                "n_steps":           ep_steps,
                "total_transitions": total_transitions,
                "elapsed_s":         elapsed,
                "critic_loss":       last_metrics.get("critic_loss", float("nan")),
                "actor_loss":        last_metrics.get("actor_loss",  float("nan")),
                "alpha_loss":        last_metrics.get("alpha_loss",  float("nan")),
                "q1":                last_metrics.get("q1",          float("nan")),
                "q2":                last_metrics.get("q2",          float("nan")),
                "entropy":           last_metrics.get("entropy",     float("nan")),
                "alpha":             last_metrics.get("alpha",       float("nan")),
                "best_return":       best_return,
            }, step=ep)

        # --- Console ---
        if ep % args.log_interval == 0 or ep == 1:
            cl = last_metrics.get("critic_loss", float("nan"))
            al = last_metrics.get("actor_loss",  float("nan"))
            en = last_metrics.get("entropy",     float("nan"))
            a  = last_metrics.get("alpha",       float("nan"))
            warm_tag = "" if total_transitions >= args.warmup else " [warmup]"
            print(
                f"{ep:6d}  {ep_return:10.2f}  {ma50:10.2f}"
                f"  {cl:9.4f}  {al:9.4f}  {en:9.4f}  {a:7.4f}"
                f"  {total_transitions:7d}{warm_tag}"
            )

    log_f.close()
    env.close()

    if use_wandb:
        wandb.save(str(out_dir / "best_agent.pt"), base_path=str(out_dir))
        wandb.finish()

    print("-" * 85)
    print(f"\nDone.  Best return: {best_return:.2f}")
    print(f"  checkpoint → {out_dir / 'best_agent.pt'}")
    print(f"  log        → {log_path}")


