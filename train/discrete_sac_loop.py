"""Train the Local-Neighbor GAT Discrete SAC baseline with a raw loop.

Wires together:
    - ``TrafficSignalEnv`` over real SUMO (via libsumo, no GUI)
    - ``LocalNeighborGATDiscreteSAC`` (local encoder + neighbor GAT + fusion)
    - Discrete SAC losses, replay, and manual optimization steps

Requirements
------------
- ``libsumo`` available in the active Python env (``import libsumo`` must work)
- ``nets/grid5x5/`` generated by ``python scripts/build_network.py``

Outputs
-------
- Console logs (every ``--log-interval`` episodes)
- ``<out-dir>/train_log.csv``  — per-episode metrics
- ``<out-dir>/best_agent.pt``  — checkpoint of best-return agent state dict
"""

