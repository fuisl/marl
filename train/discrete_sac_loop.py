@torch.no_grad()
def run_episode(
    env: TrafficSignalEnv,
    agent: LocalNeighborGATDiscreteSAC,
    device: torch.device,
    adapter: ObservationAdapter,
    feature_mode: str,
    graph_metadata: GraphMetadata,
    *,
    deterministic: bool = False,
) -> tuple[list[TensorDict], float, int, dict[str, float]]:
    """Run one full episode.

    Returns
    -------
    transitions : list[TensorDict]
        Packed single-step transitions (CPU tensors).
    n_steps : int
        Number of RL decision steps taken.
    validation_metrics : dict[str, float]
        Episode-aggregated RESCO benchmark metrics.
    """
    td = env.reset().to(device)
    transitions: list[TensorDict] = []

    while True:
        obs = adapter.graph_features(
            td["agents", "observation"],
            feature_mode=feature_mode,
        ).to(device)
        edge_index = graph_metadata.edge_index.to(device)
        edge_attr = (
            None
            if graph_metadata.edge_attr is None
            else graph_metadata.edge_attr.to(device)
        )
        mask = td["agents", "action_mask"]
        agent_node_indices = graph_metadata.agent_node_indices.to(device)
        agent_node_mask = graph_metadata.agent_node_mask.to(device)

        actions, _ = agent.select_action(
            obs,
            edge_index,
            edge_attr,
            mask,
            deterministic,
            agent_node_indices=agent_node_indices,
            agent_node_mask=agent_node_mask,
        )

        next_td = env.step(actions.cpu()).to(device)
        next_obs = adapter.graph_features(
            next_td["agents", "observation"],
            feature_mode=feature_mode,
        ).to(device)
        transitions.append(
            pack_transition(
                td.cpu(),
                actions.cpu(),
                next_td.cpu(),
                graph_observation=obs.cpu(),
                next_graph_observation=next_obs.cpu(),
                graph_metadata=graph_metadata,
            )
        )

        if next_td["done"].item():
            break
        td = next_td

    validation_metrics = dict(env.get_episode_metrics())
    return transitions, float(validation_metrics.get("global_reward", 0.0)), len(transitions), validation_metrics


# ======================================================================
# SAC update
# ======================================================================


def sac_update(
    loss_fn: DiscreteSACLossComputer,
    replay: ReplayBuffer,
    optimizers: Mapping[str, torch.optim.Optimizer],
    batch_size: int,
    device: torch.device,
) -> dict[str, float] | None:
    """Sample a mini-batch and run one SAC gradient step.

    Returns a metrics dict, or ``None`` if the buffer is too small.
    """
    if len(replay) < batch_size:
        return None

    batch = collate_batch(replay.sample(batch_size)).to(device)
    out = loss_fn.compute(batch)

    optimizers["critic"].zero_grad()
    out.critic_loss.backward()
    torch.nn.utils.clip_grad_norm_(
        loss_fn.agent.critic.parameters(), max_norm=10.0
    )
    optimizers["critic"].step()

    optimizers["actor"].zero_grad()
    out.actor_loss.backward()
    torch.nn.utils.clip_grad_norm_(
        list(loss_fn.agent.local_encoder.parameters())
        + list(loss_fn.agent.neighbor_encoder.parameters())
        + list(loss_fn.agent.fusion.parameters())
        + list(loss_fn.agent.actor.parameters()),
        max_norm=10.0,
    )
    optimizers["actor"].step()

    optimizers["alpha"].zero_grad()
    out.alpha_loss.backward()
    optimizers["alpha"].step()

    loss_fn.agent.soft_update_target()

    return {
        "critic_loss": out.critic_loss.item(),
        "actor_loss":  out.actor_loss.item(),
        "alpha_loss":  out.alpha_loss.item(),
        "q1":          out.q1_mean,
        "q2":          out.q2_mean,
        "entropy":     out.entropy,
        "alpha":       out.alpha,
    }


